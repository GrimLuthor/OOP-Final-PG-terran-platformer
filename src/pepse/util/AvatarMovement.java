package pepse.util;
/**
 * AvatarMovement enum represents the possible movement states of the avatar in the game.
 * It includes IDLE, JUMPING, and RUNNING states.
 */
public enum AvatarMovement {
    /**
     * Represents the idle state where the avatar is not moving.
     */
    IDLE,
    /**
     * Represents the jumping state where the avatar is in mid-air after a jump.
     */
    JUMPING,
    /**
     * Represents the running state where the avatar is moving horizontally.
     */
    RUNNING
}
